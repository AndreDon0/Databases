/* Задание 0
 * Одна из таблиц не связана с другими, необходимо это исправить.
 * Напишите скрипт, которые это исправляет.
 * 
 * Обеспечьте уникальность добавления процедуры и дполнительной услуги в таблицу.
 * 
 * Переименуйте таблицу процедур, т.к. слово procedures является ключевым.
 * 
 */

 SET SEARCH_PATH TO PUBLIC;

ALTER TABLE IF EXISTS employees
    ADD FOREIGN KEY (salon_id)
    REFERENCES salons (salon_id) MATCH SIMPLE
    ON UPDATE NO ACTION
    ON DELETE NO ACTION
    NOT VALID;


ALTER TABLE "procedures" RENAME TO main_services;
ALTER TABLE emp_serv RENAME TO employees_x_services;


BEGIN;
    ALTER TABLE additional_services DROP COLUMN add_service_id;
    ALTER TABLE main_services DROP COLUMN procedure_id;

    ALTER TABLE services ADD COLUMN category CHAR(1);

    UPDATE services SET category = 'm'
    WHERE service_id IN (
        SELECT service_id FROM main_services
    );
    UPDATE services SET category = 'a'
    WHERE service_id IN (
        SELECT service_id FROM additional_services
    );

    ALTER TABLE services ADD CONSTRAINT services_unique UNIQUE (service_id, category);
    ALTER TABLE services ADD CONSTRAINT services_category_check CHECK (category IN ('m', 'a'));

    ALTER TABLE main_services DROP CONSTRAINT procedures_service_id_fkey;
    ALTER TABLE additional_services DROP CONSTRAINT additional_services_service_id_fkey;

    ALTER TABLE main_services ADD PRIMARY KEY (service_id);

    ALTER TABLE additional_services ADD COLUMN category CHAR(1) DEFAULT 'a';
    ALTER TABLE main_services ADD COLUMN category CHAR(1) DEFAULT 'm';

    ALTER TABLE additional_services ADD FOREIGN KEY
    (service_id, category) REFERENCES services (service_id, category);
    ALTER TABLE main_services ADD FOREIGN KEY
    (service_id, category) REFERENCES services (service_id, category);

    ALTER TABLE additional_services ADD CHECK (category = 'a');
    ALTER TABLE main_services ADD CHECK (category = 'm');
COMMIT;

-- show all FKs on the two tables
SELECT
    tc.table_name,
    tc.constraint_name
FROM information_schema.table_constraints tc
WHERE tc.constraint_type = 'FOREIGN KEY'
  AND tc.table_name IN ('additional_services', 'main_services')
ORDER BY tc.table_name, tc.constraint_name;

